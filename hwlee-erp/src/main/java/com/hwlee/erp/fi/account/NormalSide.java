package com.hwlee.erp.fi.account;

/**
 * 계정의 "정상 잔액 방향" — 그 계정이 늘어날 때 어느 쪽에 적히는가.
 *
 * <p>이 값은 {@link AccountType} 에서 파생된다. 별도 컬럼으로 두지 않는 이유:
 * 회계 원칙상 자산은 언제나 차변(증가), 부채/자본/수익은 언제나 대변(증가)으로 고정.
 * 유형이 곧 방향이라 중복 저장은 무결성 위험만 늘린다.
 */
public enum NormalSide {
    DEBIT,   // 차변 — 자산·비용 계정의 증가 방향
    CREDIT;  // 대변 — 부채·자본·수익 계정의 증가 방향
}
